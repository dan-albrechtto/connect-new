def obter_conexao() -> Generator[Session, None, None]:  # â† ESTA LINHA FOI ALTERADA
    """
    Fornece uma sessÃ£o do banco para cada requisiÃ§Ã£o FastAPI.
    
    Uso em FastAPI:
        @app.get("/usuarios")
        def listar_usuarios(db: Session = Depends(obter_conexao)):
            return db.query(Usuario).all()
    """
    db = SessionLocal()
    try:
        yield db
    except Exception as e:
        logger.error(f"âŒ Erro durante requisiÃ§Ã£o: {e}")
        db.rollback()
        raise
    finally:
        db.close()


